package com.example.musicapp.Model;

import com.example.musicapp.utils.ERole;

public class Role {
    private String id;

    private ERole name;

    private User user;

    public Role(String id, ERole name, User user) {
        this.id = id;
        this.name = name;
        this.user = user;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public ERole getName() {
        return name;
    }

    public void setName(ERole name) {
        this.name = name;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }
}
